#Conversion of logical datatype to others

#conversion to numeric
log1=as.numeric(TRUE)
log1
print(class(log1))

#conversion to character
log2=as.character(TRUE)
log2
print(class(log2))

#conversion to complex
log3=as.complex(FALSE)
log3
print(class(log3))

#conversion to integer
log4=as.integer(FALSE)
log4
print(class(log4))

